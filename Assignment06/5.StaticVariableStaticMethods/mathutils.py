'''Create a class MathUtils with a static method add(a, b) that returns the sum. No class or instance variables should be used.'''

class MathUtils:
    @staticmethod
    def add(a, b):
        return a + b
    
# Example usage
result = MathUtils.add(5, 10)
print(f"The sume of 5 and 10 is: {result}")    








